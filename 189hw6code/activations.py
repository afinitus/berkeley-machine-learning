class TanH(Activation):
    def __init__(self):
        super().__init__()

    def forward(self, Z: np.ndarray) -> np.ndarray:
        """Forward pass for f(z) = tanh(z).
        
        Parameters
        ----------
        Z  input pre-activations (any shape)

        Returns
        -------
        f(z) as described above applied elementwise to `Z`
        """
        return 2 / (1 + np.exp(-2 * Z)) - 1

    def backward(self, Z: np.ndarray, dY: np.ndarray) -> np.ndarray:
        """Backward pass for f(z) = tanh(z).
        
        Parameters
        ----------
        Z   input to `forward` method
        dY  derivative of loss w.r.t. the output of this layer

        Returns
        -------
        derivative of loss w.r.t. input of this layer
        """
        fn = self.forward(Z)
        return dY * (1 - fn ** 2)


class ReLU(Activation):
    def __init__(self):
        super().__init__()

    def forward(self, Z: np.ndarray) -> np.ndarray:
        """Forward pass for relu activation:
        f(z) = z if z >= 0
               0 otherwise
        
        Parameters
        ----------
        Z  input pre-activations (any shape)

        Returns
        -------
        f(z) as described above applied elementwise to `Z`
        """
        #we just want to check the max of the Z and 0 for this forward
        return np.maximum(0, Z)

    def backward(self, Z: np.ndarray, dY: np.ndarray) -> np.ndarray:
        """Backward pass for relu activation.
        
        Parameters
        ----------
        Z   input to `forward` method
        dY  derivative of loss w.r.t. the output of this layer
            same shape as `Z`

        Returns
        -------
        derivative of loss w.r.t. input of this layer
        """
        #here we want to make a copy of Z and use the value of Z to 
        #change backward vectors directioonality and multiply this by
        #the gradient to get our final pass
        b = np.copy(Z)
        b[Z < 0] = 0
        b[Z >= 0] = 1
        return dY * b


class SoftMax(Activation):
    def __init__(self):
        super().__init__()

    def forward(self, Z: np.ndarray) -> np.ndarray:
        """Forward pass for softmax activation.
        Hint: The naive implementation might not be numerically stable.
        
        Parameters
        ----------
        Z  input pre-activations (any shape)

        Returns
        -------
        f(z) as described above applied elementwise to `Z`
        """
        #we first want to adjust our value of Z according to the max Z
        # in our vector ad then we want to take the exponential of this
        #using the formula we than divide this by the sum of the exponentials
        Z_adj = Z - np.max(Z, axis=-1, keepdims=True)
        e_z = np.exp(Z_adj)
        return np.divide(e_z, np.sum(e_z, axis=-1, keepdims=True))

    def backward(self, Z: np.ndarray, dY: np.ndarray) -> np.ndarray:
        """Backward pass for softmax activation.
        
        Parameters
        ----------
        Z   input to `forward` method
        dY  derivative of loss w.r.t. the output of this layer
            same shape as `Z`

        Returns
        -------
        derivative of loss w.r.t. input of this layer
        """
        #first we want to take the forward of Z, and we will
        #build up our backwards vector (arr) so we set it to 
        #an empty matrix
        Z_next = self.forward(Z)
        b_arr = []
        #nowe we iterate through our forward of Z vals and use this
        #to get our matricies and our J values for the update gradient
        #then we use this to create our backwards array and we return
        #the np array of this so it is editable in the proper format
        for num, m in enumerate(Z_next):
          d_m = np.diagflat(m)
          m = m.reshape((-1, 1))
          J = d_m - np.dot(m, m.T)
          b_arr.append(dY[num] @ J)
        return np.array(b_arr)


class ArcTan(Activation):
    def __init__(self):
        super().__init__()

    def forward(self, Z):
        return np.arctan(Z)

    def backward(self, Z, dY):
        return dY * 1 / (Z ** 2 + 1)
